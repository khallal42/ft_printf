#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = -1;
	if (s)
	{
		while (s[++i])
			write(1, &s[i], 1);
		return (i);
	}
	return (ft_putstr("(null)"));
}
